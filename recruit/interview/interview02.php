							<p class="interview_catch ff-serif">家具の設計を担当しています。</p>
							<p class="interview_text">営業さんからお客様が希望する家具の仕様と建築の設計図をもらい、造作家具の図面を作成していきます。現場での打ち合わせ、お客様の様々な要望を取り入れ図面訂正を重ね、仕様が確定（受注）したら、工場への制作を依頼します。細かい家具と壁との収まりや、部材や金具の種類、設計図と実際にできあがってくる家具との違いなど、入社してからも学ぶことはたくさんありました。また、収まり具合など、実際の現場を見ないとわからないことも多いので、製作が始まってからも誤差が出ないように施行中の建築現場に実測作業に行くこともあります。施工途中の建築物では、建物自体に変更が出ることも多く、１物件につき5~6回は図面訂正があることも。大変な作業ですが、きれいに収めるためには必要不可欠な作業です。普段は１物件につき１人の担当が設計を行いますが、大きいもの・量があるものになると、分担して設計を行うこともあります。最後は家具の色や装飾、金具類もサンプルをとって、お客様と一緒に選定していきます。依頼から製作に至るまで、最短でも１~２ヶ月、大きいものになると半年程かかる案件もたまにあります。</p>
